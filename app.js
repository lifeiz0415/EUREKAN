function getSliderGroupItems(items = []) {
  if (!items.length) return [];
  const sliderWidth = Math.max(pageListNode?.clientWidth || 0, articleBodyNode?.clientWidth || 0, window.innerWidth || 0);
  const railCardWidth = Math.min(390, Math.max(CARD_MIN_WIDTH, sliderWidth * 0.26));
  const viewportFillCount = Math.ceil((sliderWidth + railCardWidth) / (railCardWidth + CARD_GAP)) + 1;
  const minCount = Math.max(items.length, SLIDER_MIN_GROUP_ITEM_COUNT, viewportFillCount);
  return Array.from({ length: minCount }, (_, index) => ({
    page: items[index % items.length],
    duplicate: index >= items.length,
  }));
}

function renderTrendingSlider(items) {
  const groupItems = getSliderGroupItems(items);
  const duration = Math.max(36, groupItems.length * 7);
  const cards = Array.from({ length: SLIDER_SEQUENCE_REPEAT_COUNT }, (_, sequenceIndex) => {
    const isDuplicateSequence = sequenceIndex > 0;
    return groupItems
      .map(({ page, duplicate }, itemIndex) => renderCard(page, "page-card--rail", isDuplicateSequence || duplicate, {
        priority: sequenceIndex === 0 && !duplicate && itemIndex === 0,
        eagerImage: sequenceIndex === 0 || (sequenceIndex === 1 && itemIndex < 4),
      }))
      .join("");
  }).join("");

  return `
      <div class="trending-window">
        <div class="trending-track" data-slider-group-size="${groupItems.length}" style="--slide-duration: ${duration}s">
          ${cards}
        </div>
      </div>
  `;
}

function refreshSliderLoops() {
  const refreshTracks = () => {
    document.querySelectorAll(".trending-track").forEach((track) => {
      refreshSliderLoop(track);
    });
  };

  refreshTracks();
  window.requestAnimationFrame(refreshTracks);
}

function refreshSliderLoop(track, attempt = 0) {
  const groupSize = Number(track.dataset.sliderGroupSize || 0);
  const cards = [...track.querySelectorAll(".page-card--rail")];
  const firstCard = cards[0];
  const nextFirstCard = groupSize > 0 ? cards[groupSize] : null;
  const rectDistance = firstCard && nextFirstCard
    ? nextFirstCard.getBoundingClientRect().left - firstCard.getBoundingClientRect().left
    : 0;
  const layoutDistance = firstCard && nextFirstCard
    ? nextFirstCard.offsetLeft - firstCard.offsetLeft
    : 0;
  const distance = Math.round(rectDistance || layoutDistance || 0);

  if (!distance) {
    if (attempt < 6) window.setTimeout(() => refreshSliderLoop(track, attempt + 1), 80);
    return;
  }

  track.style.setProperty("--slide-distance", `${distance}px`);
  track.style.setProperty("--slide-duration", `${Math.max(36, distance / 62)}s`);
  track.dataset.sliderReady = "true";
}

function getLatestPagesByDesk(items) {
  const latestByDesk = new Map();

  items.forEach((page, index) => {
    const current = latestByDesk.get(page.desk);
    if (!current || comparePagesByPublishedTime(page, current.page, index, current.index) < 0) {
      latestByDesk.set(page.desk, { page, index });
    }
  });

  return [...latestByDesk.values()]
    .sort((left, right) => comparePagesByPublishedTime(left.page, right.page, left.index, right.index))
    .map((item) => item.page);
}

function getPublishedTime(page, index) {
  const time = Date.parse(page.publishedAt || "");
  return Number.isNaN(time) ? -index : time;
}

function comparePagesByPublishedTime(left, right, leftIndex = 0, rightIndex = 0) {
  const timeDiff = getPublishedTime(right, rightIndex) - getPublishedTime(left, leftIndex);
  if (timeDiff !== 0) return timeDiff;
  return String(left.slug || "").localeCompare(String(right.slug || ""));
}

