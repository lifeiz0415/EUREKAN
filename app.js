function renderSectionPagination(desk, currentPage, totalPages) {
  if (totalPages <= 1) return "";
  const pageGroupStart = Math.floor(currentPage / PAGINATION_WINDOW_SIZE) * PAGINATION_WINDOW_SIZE;
  const pageGroupEnd = Math.min(pageGroupStart + PAGINATION_WINDOW_SIZE, totalPages);
  const previousGroupButton = pageGroupStart > 0
    ? `
        <button class="pagination-button pagination-button--step" type="button" data-desk-page="${encodeURIComponent(desk)}" data-page-index="${Math.max(0, pageGroupStart - PAGINATION_WINDOW_SIZE)}" aria-label="이전 페이지 묶음">
          &lt;
        </button>
      `
    : "";
  const nextGroupButton = pageGroupEnd < totalPages
    ? `
        <button class="pagination-button pagination-button--step" type="button" data-desk-page="${encodeURIComponent(desk)}" data-page-index="${pageGroupEnd}" aria-label="다음 페이지 묶음">
          &gt;
        </button>
      `
    : "";

  return `
    <nav class="section-pagination" aria-label="${escapeHtml(desk)} 페이지 이동">
      ${previousGroupButton}
      ${Array.from({ length: pageGroupEnd - pageGroupStart }, (_, offset) => {
        const pageIndex = pageGroupStart + offset;
        return `
        <button class="pagination-button${pageIndex === currentPage ? " pagination-button--active" : ""}" type="button" data-desk-page="${encodeURIComponent(desk)}" data-page-index="${pageIndex}" aria-current="${pageIndex === currentPage ? "page" : "false"}">
          ${pageIndex + 1}
        </button>
      `;
      }).join("")}
      ${nextGroupButton}
    </nav>
  `;
}

function shouldRebaseArticleBodyUrl(value = "") {
  const trimmedValue = String(value).trim();
  return Boolean(trimmedValue && !trimmedValue.startsWith("#") && !/^(?:[a-z][a-z0-9+.-]*:|\/\/)/i.test(trimmedValue));
}

function rebaseArticleBodyUrl(value = "", baseUrl = getHomeUrl()) {
  const trimmedValue = String(value).trim();
  if (!shouldRebaseArticleBodyUrl(trimmedValue)) return value;

  try {
    return new URL(trimmedValue, baseUrl).toString();
  } catch {
    return value;
  }
}

function rebaseArticleBodySrcset(value = "", baseUrl = getHomeUrl()) {
  return String(value)
    .split(",")
    .map((candidate) => {
      const parts = candidate.trim().split(/\s+/).filter(Boolean);
      if (!parts.length) return "";
      const [url, ...descriptors] = parts;
      return [rebaseArticleBodyUrl(url, baseUrl), ...descriptors].join(" ");
    })
    .filter(Boolean)
    .join(", ");
}

function rebaseArticleBodyAssetUrls(rootNode, baseUrl = getHomeUrl()) {
  rootNode.querySelectorAll(ARTICLE_BODY_ASSET_SELECTOR).forEach((node) => {
    ARTICLE_BODY_ASSET_ATTRIBUTES.forEach((attributeName) => {
      if (!node.hasAttribute(attributeName)) return;
      node.setAttribute(attributeName, rebaseArticleBodyUrl(node.getAttribute(attributeName), baseUrl));
    });
  });

  rootNode.querySelectorAll("[srcset]").forEach((node) => {
    node.setAttribute("srcset", rebaseArticleBodySrcset(node.getAttribute("srcset"), baseUrl));
  });
}

function extractArticleBodyHtml(html = "", baseUrl = getHomeUrl()) {
  const documentFragment = new DOMParser().parseFromString(String(html), "text/html");
  const bodyNode = documentFragment.querySelector(ARTICLE_BODY_SELECTOR);
  if (!bodyNode) throw new Error("정적 HTML 본문을 찾지 못했습니다.");
  rebaseArticleBodyAssetUrls(bodyNode, baseUrl);
  return bodyNode.innerHTML.trim();
}

async function loadArticleBodyHtml(page) {
  const articleUrl = getArticleUrl(page.slug);
  const response = await fetch(articleUrl, { cache: "no-store" });
  if (!response.ok) throw new Error("정적 HTML 본문을 불러오지 못했습니다.");
  return extractArticleBodyHtml(await response.text(), articleUrl);
}

